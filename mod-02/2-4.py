print("Lasketaan lukujen summa, tulo ja keskiarvo.")
x = int(input("Syötä ensimmäinen luku: "))
y = int(input("Syötä toinen luku: "))
z = int(input("Syötä kolmas luku: "))

sum = x + y + z
multiply = x * y * z
average = sum / 3

print("Lukujen summa on ", sum)
print("Lukujen tulo on ", multiply)
print("Lukujen keskiarvo on ", average)